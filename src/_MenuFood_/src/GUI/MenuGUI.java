    private void button_otropedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_otropedidoActionPerformed
        // TODO add your handling code here:

        Dish dentrada, dpfuerte, dbebida, dpostre;

        try {
            dentrada = (Dish) modelentr.getElementAt(list_entradas.getSelectedIndex());
            dpfuerte = (Dish) modelpf.getElementAt(list_platofuerte.getSelectedIndex());
            dbebida = (Dish) modelbeb.getElementAt(list_bebidas.getSelectedIndex());
            dpostre = (Dish) modelpos.getElementAt(list_postres.getSelectedIndex());
            orderDish.pushBack(dentrada);
            orderDish.pushBack(dpfuerte);
            orderDish.pushBack(dbebida);
            orderDish.pushBack(dpostre);

            JOptionPane.showMessageDialog(rootPane, "Pedido Agregado al carrito!", "Informacion Carrito", JOptionPane.INFORMATION_MESSAGE);

            list_entradas.clearSelection();
            list_platofuerte.clearSelection();
            list_bebidas.clearSelection();
            list_postres.clearSelection();

        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(rootPane, "Porfavor selecciona algo en cada tipo de plato!", "Error al agregar al carrito", JOptionPane.OK_OPTION);
        }


    }//GEN-LAST:event_button_otropedidoActionPerformed

    private void button_terminarpedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_terminarpedidoActionPerformed
        // TODO add your handling code here:
        try {
            if (orderDish.isEmpty()) {
                    JOptionPane.showMessageDialog(rootPane, "Aun no has hecho nigun pedido!", "Error al completar el pedido", JOptionPane.OK_OPTION);
                
            } else {
          
                OrderConfirmationGUI ocg = new OrderConfirmationGUI();
                
                //Se crea la orden
                Order ord = new Order(orderDish, this.c );
                this.dispose();
                ocg.setVisible(true);
                ocg.loadOrder(ord);
            }

            clearLists();

        } catch (Exception e) {
            System.out.println(e.getMessage());
        }

    }//GEN-LAST:event_button_terminarpedidoActionPerformed

