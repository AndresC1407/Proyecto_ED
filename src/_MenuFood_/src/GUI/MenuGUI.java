    public void fillMenu(LinkedList<Dish> dishes) {

        Dish tempdish;
        String type;

        modelentr.addElement(new Dish("Sin Entrada"));
        modelpf.addElement(new Dish("Sin Plato Fuerte"));
        modelbeb.addElement(new Dish("Sin bebida"));
        modelpos.addElement(new Dish("Sin Postre"));

        if (dishes != null) {
            Node temp = dishes.getHead();
            while (temp != null) {
                tempdish = (Dish) temp.data;
                type = tempdish.getType();
                if (type.equals("Entrada")) {
                    modelentr.addElement(tempdish);
                } else if (type.equals("Plato Fuerte")) {
                    modelpf.addElement(tempdish);
                } else if (type.equals("Bebida")) {
                    modelbeb.addElement(tempdish);
                } else if (type.equals("Postres")) {
                    modelpos.addElement(tempdish);
                }
                temp = temp.next;
            }
        }
        
        list_entradas.setModel(modelentr);
        list_platofuerte.setModel(modelpf);
        list_bebidas.setModel(modelbeb);
        list_postres.setModel(modelpos);

    }
    
    public void clearLists() {
        list_entradas.clearSelection();
        list_platofuerte.clearSelection();
        list_bebidas.clearSelection();
        list_postres.clearSelection();
    }

